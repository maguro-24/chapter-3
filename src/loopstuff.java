

public class loopstuff {

    public static void main(String[] args) {
        int count = 1;
        
        while(count <= 10)
        {
            System.out.println(count);       
            count += 1;
        }
        
        while(count > 0)
        {
            System.out.println(count);
            count--;
        }
                
                
    }
}
